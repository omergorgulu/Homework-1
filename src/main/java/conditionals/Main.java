package conditionals;

public class Main {
    public static void main(String[] args) {
        int sayi = 17;

        if (sayi > 14) {
            System.out.println("Sayı 14'ten büyüktür.");
        } else if (sayi < 14) {
            System.out.println("Sayı 14'ten küçüktür.");
        } else {
            System.out.println("Sayı 14'e eşittir.");
        }
    }
}
